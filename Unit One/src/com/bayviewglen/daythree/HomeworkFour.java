public class HomeworkFour {

	public static void main(String[] args) {
			
		// Find the slope
		
		double x1, x2, y1, y2;
		double slope;
		
		x1 = 8;
		y1 = 4;
		
		x2 = 10;
		y2 = 5;
		
		slope = (y2 - y1) / (x2 - x1);
		
		System.out.println("The slope of a line given the points (" + x2 + "," + y2 + ") and (" + x1 + "," + y1 + ") is " + slope);
		

	}

}
